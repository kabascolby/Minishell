int		ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return ((c - 'A') + 'a');
	return (c);
}

void	ft_tolower2(char *c)
{
	if (*c >= 'A' && *c <= 'Z')
		*c = (*c - 'A') + 'a';
}
